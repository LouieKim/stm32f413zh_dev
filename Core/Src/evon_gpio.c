uint8_t adc_loop()
{
	MDregister[EVON_REGISTER_MBUS_MAP_CCS_01_PD_R] = adc_val[0];
	MDregister[EVON_REGISTER_MBUS_MAP_CCS_01_TEMP_R] = adc_val[1];
	MDregister[EVON_REGISTER_MBUS_MAP_CCS_02_PD_R] = adc_val[2];
	MDregister[EVON_REGISTER_MBUS_MAP_CCS_02_TEMP_R] = adc_val[3];
	MDregister[EVON_REGISTER_MBUS_MAP_DCPT_01_R] = adc_val[4];
	MDregister[EVON_REGISTER_MBUS_MAP_DCPT_02_R] = adc_val[5];
	MDregister[EVON_REGISTER_MBUS_MAP_TEST_TEMP_R] = adc_val[6];

	return 1;
}


