//포인터로 Parameter 받기
uint8_t gpio_out()
{
	//Config 상에서 변경만으로 알 수 있도록 해야함
	(coil(EVON_COIL_MBUS_MAP_DOOR_01_W)) ? DOOR_01_ON : DOOR_01_OFF;
	(coil(EVON_COIL_MBUS_MAP_DOOR_02_W)) ? DOOR_02_ON : DOOR_02_OFF;

	(coil(EVON_COIL_MBUS_MAP_FAN_01_W)) ? FAN_01_ON : FAN_01_OFF;
	(coil(EVON_COIL_MBUS_MAP_FAN_02_W)) ? FAN_02_ON : FAN_02_OFF;

	(coil(EVON_COIL_MBUS_MAP_MC_01_W)) ? MC_01_ON : MC_01_OFF;
	(coil(EVON_COIL_MBUS_MAP_MC_02_W)) ? MC_02_ON : MC_02_OFF;

	(coil(EVON_COIL_MBUS_MAP_RELAY_01_02_W)) ? RELAY_01_02_ON : RELAY_01_02_OFF;
	(coil(EVON_COIL_MBUS_MAP_RELAY_03_04_W)) ? RELAY_03_04_ON : RELAY_03_04_OFF;
	(coil(EVON_COIL_MBUS_MAP_RELAY_05_06_W)) ? RELAY_05_06_ON : RELAY_05_06_OFF;

	(coil(EVON_COIL_MBUS_MAP_R_LED_01_W)) ? R_LED_01_ON : R_LED_01_OFF;
	(coil(EVON_COIL_MBUS_MAP_G_LED_01_W)) ? G_LED_01_ON : G_LED_01_OFF;
	(coil(EVON_COIL_MBUS_MAP_B_LED_01_W)) ? B_LED_01_ON : B_LED_01_OFF;

	(coil(EVON_COIL_MBUS_MAP_R_LED_02_W)) ? R_LED_02_ON : R_LED_02_OFF;
	(coil(EVON_COIL_MBUS_MAP_G_LED_02_W)) ? G_LED_02_ON : G_LED_02_OFF;
	(coil(EVON_COIL_MBUS_MAP_B_LED_02_W)) ? B_LED_02_ON : B_LED_02_OFF;

	(coil(EVON_COIL_MBUS_MAP_STATUS_LED_01_W)) ? STATUS_LED_01_ON : STATUS_LED_01_OFF;
	(coil(EVON_COIL_MBUS_MAP_STATUS_LED_02_W)) ? STATUS_LED_02_ON : STATUS_LED_02_OFF;
	(coil(EVON_COIL_MBUS_MAP_STATUS_LED_03_W)) ? STATUS_LED_03_ON : STATUS_LED_03_OFF;
	(coil(EVON_COIL_MBUS_MAP_STATUS_LED_04_W)) ? STATUS_LED_04_ON : STATUS_LED_04_OFF;
	(coil(EVON_COIL_MBUS_MAP_FAULT_LED_W)) ? FAULT_LED_ON : FAULT_LED_OFF;

	return 1;
}


